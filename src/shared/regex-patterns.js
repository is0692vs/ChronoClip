/**
 * @fileoverview 日付検出用の正規表現パターンを定義します。
 */

// グローバル汚染を避けるために、アプリ用のグローバルオブジェクトを作成します
var ChronoClip = ChronoClip || {};

// YYYY/MM/DD または YYYY-MM-DD 形式の日付に一致します。
ChronoClip.DATE_PATTERN = /\b(\d{4})([-/])(0[1-9]|1[0-2])\2(0[1-9]|[12]\d|3[01])\b/g;

/**
 * 日本の元号（和暦）の日付形式に一致します。
 * 例: 「令和6年8月27日」、「平成元年1月8日」
 * 元号、年（「元」を含む）、月、日をキャプチャします。
 */
ChronoClip.WAREKI_DATE_PATTERN = /(令和|平成|昭和|大正)(\d{1,2}|元)年\s*(\d{1,2})月\s*(\d{1,2})日/g;

/**
 * 年が省略された「月日」の形式に一致します。曜日は任意です。
 * 例: 「8月27日」、「8月27日（火）」
 * 月、日をキャプチャします。
 */
ChronoClip.MONTH_DAY_DATE_PATTERN = /(\d{1,2})月\s*(\d{1,2})日(?:\s*[(（][月火水木金土日][)）])?/g;

/**
 * 全角または半角スペースで区切られた西暦の日付形式に一致します。
 * 例: 「2025 年 8 月 27 日」
 * 年、月、日をキャプチャします。
 */
ChronoClip.JA_YYYY_MM_DD_PATTERN = /(\d{4})\s*年\s*(\d{1,2})\s*月\s*(\d{1,2})\s*日/g;
